              <h2>”鬼滅の刃”(きめつのやいば)とは？</h2>
              <p>”鬼滅の刃”は、週刊少年ジャンプに連載していた、吾峠呼世晴さんのマンガ作品です。
                <br><br>
                2016年から連載開始しているとは知らなかったですし、僕は、新型コロナ自粛期間中の2020年3月に<a href="https://amzn.to/3ddBatD">アニメ</a>で知りました。
                <br><br>
                大正時代を舞台にして、人間と人を食べる鬼との戦いや切ない背景を描いている物語です。主人公の竈門炭治郎（かまど たんじろう）の思い遣りややさしさが過ぎるので、大人が観ると感動して泣けてしまいます。
                <br><br>
                また、アニメでは映像美や声優陣も相まって、より楽しめる作品となっているようです。僕は今のところ、アニメと映画しか観ていませんが、きっとアニメで観るべき芸術的作品なのではないかと思っています。
                <br><br>
                マンガでは、シリーズ累計発行部数が、最後の23巻発売で1億2000万部を越えたとのこと。映画『劇場版 鬼滅の刃 無限列車編』では、『タイタニック』や『千と千尋の神隠し』を、日本歴代興行収入1位となりました。
                <br><br>
                恐い鬼や、飛び散る血、グロテスクな描写も多く、小さい子供には向いていない、人気はでないのではないかと思いました。
                <br><br>
                2021年2月現在でも、その人気の勢いは止まらないですし、5歳前後のうちの甥っ子姪っ子たちも”鬼滅の刃”を大好きなようで、とても意外な反応です。
                <br><br>
                個人的に感じたことですが、誰にも過去があり、それが良くも悪くも人格を作っている。
                <br><br>
                したいことも、したくないことでも、過去がそうさせていること。
                <br><br>
                その過去を含めた自分を理解してくれる人がいる。それだけでも報われる。
                <br><br>
                そういう鬼（人）が、どんな最後を迎えるのか、鬼ごとに違う過去が描かれていて、心に強く響くものがありました。
                <br><br>
                さらに、大正時代の描写がとても繊細で、歴史の学びとしても視聴できる作品です。
              </p>

